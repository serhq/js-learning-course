// Conventions for naming variables
// 1) Write the first word in lower case and others in upper case:
let carColor = 'red';
// 2) Dividing words by underscore (_):
let food_products = ['milk',
    'eggs', 'bread', 'cheese'
]
// 3) MISTAKE! You're not allowed to use numbers in variable names. They cannot contain symbols, numbers, emojis, names of functions in JavaScript
// let new = 27; (new is a reserved keyword in JS)
// let 3years = 3; (numbers are not allowed)
// 4) You can use only underscore and only dollar sign.
let $function = 'Activated';
// 5) MISTAKE! We cannot start with the uppercase.
// 6) Names of variables written all in uppercase are considered to be constants.
let PI = 3.1415;
let SECONDS = 60;

